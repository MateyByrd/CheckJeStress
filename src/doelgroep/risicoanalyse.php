<h1>Burnout risico-analyse voor bedrijven</h1>

<p>
  Zoals je op de vorige pagina al hebt kunnen lezen kost stress en burnout het bedrijfsleven heel erg veel geld. Geld wat makkelijk uitgespaard kan worden door een <b>goed preventiebeleid</b>. Steunpunt Stress Burnout Nederland kan voor jouw organisatie een <b>burnout risico-analyse</b> uitvoeren.
</p>

<p>
  Hierbij wordt de organisatie (of delen daarvan) doorgelicht met gebruik van testen. Elk personeelslid vult een uitgebreide test in en als werkgever zie je door de terugrapportage in 1 oogopslag hoeveel procent van de medewerkers er gevaar loopt.
</p>

<p>
	Steunpunt Stress Burnout Nederland kan voor jullie dan daarna in gesprek gaan met de betreffende personeelsleden om directe <b>stressreductie</b> te creeëren om ziekteverzuim als gevolg van spanning terug te schroeven. Uiteraard ontvangt de organisatie een uitgebreide rapportage van de testresultaten. Aangezien dit gaat om privacygevoelige informatie hebben alleen daartoe geautoriseerde medewerkers hier inzicht in. De uitkomsten geven een goed beeld om direct en effectief op te kunnen treden.
</p>

<p>
  Ook een voor de organisatie op maat geschreven Burnoutpreventieplan om ziekteverzuim als gevolg van stress in de toekomst voor te zijn is één van de instrumenten om duurzaam om te gaan met je belangrijkste bedrijfskapitaal: je eigen personeel. Ook bieden we de mogelijkheid om leidinggevenden te trainen in het herkennen van stresssignalen en de daarop volgende actie. Voorkomen is ook hier beter dan genezen.
</p>

<p>
  Twijfel je als ondernemer of de organisatie gebukt gaat onder stress en/of burnout? Vul dan de test in voor ondernemers onder <a href="../../tests/">testen</a>. Kies test B.
</p>

<p>
  Steunpunt Stress Burnout Nederland heeft zoals gezegd voor elk bedrijf een preventiebeleidsplan, dat meteen geïmplementeerd kan worden om actief op te treden tegen stress en burnout. Tevens kunnen we ook <b>ondersteuning en begeleiding</b> verzorgen om dit beleid in de organisatie uit te zetten. Actief optreden tegen ziekteverzuim en het hebben van een preventieplan zijn onderdelen van goed <b>werkgeversschap!</b>
</p>

